    private void jTableTatuadorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTatuadorMouseClicked
        String nome_tatuador =""+jTableTatuador.getValueAt(jTableTatuador.getSelectedRow(), 1);
        conex.getConnection();
        conex.executaSql("select *from tatuadores where nome_tatuador= '"+nome_tatuador+"'");
        try {
            conex.rs.first();
            jtCOD.setText(String.valueOf(conex.rs.getInt("cod_tatuador")));
            jTextFieldNome.setText(conex.rs.getString("nome_tatuador"));
            jComboBoxEspecialista.setSelectedItem(conex.rs.getString("especialidade_tatuador"));
            jTextFieldTempo.setText(conex.rs.getString("tempo_profissao"));
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(null, "Erro ao selecionar os dados"+ex);
        }
        conex.desconecta();
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
    }//GEN-LAST:event_jTableTatuadorMouseClicked

    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas  = new String[]{"ID", "Nome","Especialidade", "Tempo de Profissão"};
        conex.getConnection();
        conex.executaSql(Sql);
        
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("cod_tatuador"),conex.rs.getString("nome_tatuador"),conex.rs.getString("especialidade_tatuador"),conex.rs.getString("tempo_profissao")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Busque por outro Tatuador para preencher a tabela");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTableTatuador.setModel(modelo);
        jTableTatuador.getColumnModel().getColumn(0).setPreferredWidth(23);
        jTableTatuador.getColumnModel().getColumn(0).setResizable(false);
        jTableTatuador.getColumnModel().getColumn(1).setPreferredWidth(150);
        jTableTatuador.getColumnModel().getColumn(1).setResizable(false);
        jTableTatuador.getColumnModel().getColumn(2).setPreferredWidth(180);
        jTableTatuador.getColumnModel().getColumn(2).setResizable(false);
        jTableTatuador.getColumnModel().getColumn(3).setPreferredWidth(200);
        jTableTatuador.getColumnModel().getColumn(3).setResizable(false);
        jTableTatuador.getTableHeader().setReorderingAllowed(false);
        jTableTatuador.setAutoResizeMode(jTableTatuador.AUTO_RESIZE_OFF);
        jTableTatuador.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        conex.desconecta();
    }
    
    
