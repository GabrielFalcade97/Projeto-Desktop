    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if(jTextFieldNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o campo NOME para continuar");
            jTextFieldNome.requestFocus();
        }else if(jTextFieldTempo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o campo TEMPO DE PROFISSÃO para continuar");
            jTextFieldTempo.requestFocus();
        }else{
  
        if(flag == 1){
            mod.setNome(jTextFieldNome.getText());
            mod.setEspecialidade((String)jComboBoxEspecialista.getSelectedItem());
            mod.setTempoProfissao(jTextFieldTempo.getText());
            control.Salvar(mod);
            jTextFieldNome.setText("");
            jTextFieldTempo.setText("");
            jTextFieldNome.setEnabled(false);
            jTextFieldTempo.setEnabled(false);
            jComboBoxEspecialista.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonCancelar.setEnabled(false);
            preencherTabela("select *from tatuadores order by nome_tatuador");
        }else{
            mod.setCodigo(Integer.parseInt(jtCOD.getText()));
            mod.setNome(jTextFieldNome.getText());
            mod.setEspecialidade((String)jComboBoxEspecialista.getSelectedItem());
            mod.setTempoProfissao(jTextFieldTempo.getText());
            control.editar(mod);
            jtCOD.setText("");
            jTextFieldTempo.setText("");
            jTextFieldNome.setText("");
            jTextFieldNome.setEnabled(false);
            jTextFieldTempo.setEnabled(false);
            jComboBoxEspecialista.setEnabled(false);
            jButtonSalvar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
            preencherTabela("select *from tatuadores order by nome_tatuador");
        }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

