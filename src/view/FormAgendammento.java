    private void jTableClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableClienteMouseClicked
        String nome_cliente =""+jTableCliente.getValueAt(jTableCliente.getSelectedRow(), 1);
        conex.getConnection();
        conex.executaSql("select *from cliente where cli_nome= '"+nome_cliente+"'");
        try {
            conex.rs.first();
            jTextFieldCliente.setText(conex.rs.getString("cli_nome"));
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(null,"Erro ao buscar cliente"+ ex);
        }
        conex.desconecta();
        
        jComboBoxTatuador.setEnabled(true);
        jDateChooser1.setEnabled(true);
        jTextFieldObs.setEnabled(true);
        jButtonFinalizar.setEnabled(true);
        jButtonCancelarAge.setEnabled(true);
    }//GEN-LAST:event_jTableClienteMouseClicked

