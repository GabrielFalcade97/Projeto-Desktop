    public void preencherTatuador(){
        conex.getConnection();
        
        conex.executaSql("select *from tatuadores order by nome_tatuador");
        try {
            conex.rs.first();
            jComboBoxTatuador.removeAllItems();
            do{
                jComboBoxTatuador.addItem(conex.rs.getString("nome_tatuador"));
            }while(conex.rs.next());
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher Tatuador"+ex);
        }
        conex.desconecta();
    }
    
    
